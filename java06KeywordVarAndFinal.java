public class java06KeywordVarAndFinal {
    public static void main(String[] args) {
        System.out.println("===keyword var===");
        var words = "hello bro";
        var one = 1;
        System.out.println(words);
        System.out.println(one);

        System.out.println("===keyword final and var===");
        final var wordz = "hi lehmann";
        System.out.println(wordz);
    }
}
